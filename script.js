let currentCategory = [];
let currentIndex = 0;
let currentRemainingCount = 0;
let originalCount = 0; 
let currentFontSize = 1.6;

const homeScreen = document.getElementById('home-screen');
const azkarScreen = document.getElementById('azkar-screen');
const zikrCard = document.getElementById('zikr-card');
const zikrText = document.getElementById('zikr-text');
const zikrProof = document.getElementById('zikr-proof');
const zikrCount = document.getElementById('zikr-count');
const progressBar = document.getElementById('progress-bar');
const progressText = document.getElementById('progress-text');
const counterBtn = document.getElementById('counter-btn');

window.toggleTheme = function() {
    document.body.classList.toggle('dark-theme');
    const isDark = document.body.classList.contains('dark-theme');
    localStorage.setItem('theme', isDark ? 'dark' : 'light');
};

window.changeFontSize = function(step) {
    if (step > 0 && currentFontSize < 4.0) currentFontSize += 0.25;
    if (step < 0 && currentFontSize > 1.2) currentFontSize -= 0.25;
    document.documentElement.style.setProperty('--zikr-font-size', `${currentFontSize}rem`);
    if(zikrText) zikrText.style.fontSize = `${currentFontSize}rem`;
    localStorage.setItem('fontSize', currentFontSize);
};

window.startAzkar = function(type) {
    currentCategory = azkarData[type];
    currentIndex = 0;
    homeScreen.classList.remove('active'); homeScreen.classList.add('hidden');
    azkarScreen.classList.remove('hidden'); azkarScreen.classList.add('active');
    loadZikr();
};

window.goHome = function() {
    azkarScreen.classList.remove('active'); azkarScreen.classList.add('hidden');
    homeScreen.classList.remove('hidden'); homeScreen.classList.add('active');
};

window.onload = () => {
    if(localStorage.getItem('theme') === 'dark') document.body.classList.add('dark-theme');
    const savedSize = localStorage.getItem('fontSize');
    if(savedSize) {
        currentFontSize = parseFloat(savedSize);
        document.documentElement.style.setProperty('--zikr-font-size', `${currentFontSize}rem`);
        if(zikrText) zikrText.style.fontSize = `${currentFontSize}rem`;
    }
};

function loadZikr() {
    if (currentIndex >= currentCategory.length) { goHome(); return; }
    
    const zikr = currentCategory[currentIndex];
    currentRemainingCount = zikr.count;
    originalCount = zikr.count;
    
    zikrText.innerText = zikr.text;
    zikrProof.innerText = zikr.proof;
    zikrCount.innerText = currentRemainingCount;
    
    const progressPercentage = ((currentIndex + 1) / currentCategory.length) * 100;
    progressBar.style.width = `${progressPercentage}%`;
    progressText.innerText = `${currentIndex + 1} من ${currentCategory.length}`;
    
    zikrCard.className = "paper-card swipe-in";
    
    // الحل السحري: الرجوع لأعلى الصفحة مع كل ذكر جديد
    window.scrollTo({ top: 0, behavior: 'smooth' });
    
    setTimeout(() => zikrCard.classList.remove('swipe-in'), 300);
}

counterBtn.addEventListener('click', (e) => {
    e.stopPropagation(); 
    if (currentRemainingCount > 1) {
        currentRemainingCount--;
        zikrCount.innerText = currentRemainingCount;
        if (navigator.vibrate) {
            let passedCount = originalCount - currentRemainingCount;
            if (originalCount > 50 && (passedCount === 33 || passedCount === 66 || passedCount === 99)) {
                navigator.vibrate([100, 50, 100]); 
            } else {
                navigator.vibrate(40); 
            }
        }
    } else {
        if (navigator.vibrate) navigator.vibrate(200); 
        nextZikr();
    }
});

function nextZikr() {
    if (currentIndex >= currentCategory.length - 1) { goHome(); return; }
    zikrCard.classList.add('swipe-out-right');
    setTimeout(() => { currentIndex++; loadZikr(); }, 300);
}

function prevZikr() {
    if (currentIndex <= 0) return;
    zikrCard.classList.add('swipe-out-left');
    setTimeout(() => { currentIndex--; loadZikr(); }, 300);
}

let touchStartX = 0; let touchEndX = 0;
azkarScreen.addEventListener('touchstart', (e) => { touchStartX = e.changedTouches[0].screenX; }, {passive: true});
azkarScreen.addEventListener('touchend', (e) => { touchEndX = e.changedTouches[0].screenX; handleSwipe(); });

function handleSwipe() {
    const swipeDistance = touchEndX - touchStartX;
    if (swipeDistance > 50) { nextZikr(); }
    else if (swipeDistance < -50) { prevZikr(); }
}
